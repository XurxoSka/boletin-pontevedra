const ofertasContainer = document.getElementById('ofertas-container');

ofertas.forEach(oferta => {
    const table = document.createElement('table');

    // Título da oferta
    const tituloRow = document.createElement('tr');
    const tituloHeader = document.createElement('th');
    tituloHeader.textContent = oferta.titulo;
    tituloHeader.setAttribute('colspan', '2');
    tituloRow.appendChild(tituloHeader);
    table.appendChild(tituloRow);

    // Lista de campos posibles
    const campos = [
        { label: 'Publicada', value: oferta.publicada },
        { label: 'Empresa', value: oferta.empresa },
        { label: 'Localidade', value: oferta.localidade },
        { label: 'Descrición', value: oferta.descricion },
        { label: 'Condicións', value: oferta.condiciones },
        { label: 'Contacto', value: oferta.contacto }
    ];

    // Engadir só os campos que teñen valor
    campos.forEach(campo => {
        if (campo.value !== null && campo.value !== undefined) {
            const row = document.createElement('tr');
            const labelCell = document.createElement('th');
            const valueCell = document.createElement('td');

            labelCell.textContent = campo.label;
            valueCell.textContent = campo.value;

            row.appendChild(labelCell);
            row.appendChild(valueCell);
            table.appendChild(row);
        }
    });

    ofertasContainer.appendChild(table);
});